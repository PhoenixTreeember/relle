<ol>
    <li>Clique no nome do usuário no canto superior direito do menu e selecione a opção "Painel de Controle".</li>
    <img src="http://relle.ufsc.br/help/img/dash.png" class="img-tutorial"/>
    <li>Clique em "Experimentos" e selecione "Criar Experimento".</li>
    <li>Na primeira tela, preencha o nome e descrição do experimento em português e inglês, insira no campo "Tags" palavras relacionadas ao experimento e selecione a duração da sessão do experimento. Todos os campos são obrigatórios.</li>
    <img src="http://relle.ufsc.br/help/img/create_lab_1.png" class="img-tutorial"/>
    <li>Na segunda tela, escolha se o experimento irá ou não utilizar o sistema de fila do RELLE, e classifique o experimento quanto ao Alvo, Dificuldade, Interação e Disciplina. Todos os campos são obrigatórios.</li>
    <img src="http://relle.ufsc.br/help/img/create_lab_2.png" class="img-tutorial"/>
    <li>Na terceira tela, envie uma imagem do experimento e o pacote de arquivos para acesso experimento. Um exemplo de pacote de arquivos para um experimento pode ser encontrado no <a href="https://github.com/RExLab/lab_package" target='_blank'>GitHub</a>.</li>
    <img src="http://relle.ufsc.br/help/img/create_lab_3.png" class="img-tutorial"/>
    <li>Clique em Enviar. Seu experimento deverá aparecer na página inicial do RELLE.</li>
</ol>

